package chapter02;

public class ComputeArea {
  public static void main(String[] args) {
    double radius; // Declare radius
    double area; // Declare area

    // Assign a radius
    radius = 20; // New value is radius

    // Compute area
    area = radius * radius * 3.14159;

    // Display results
    System.out.println("The area for the circle of radius " +
      radius + " is " + area);

	double radius2;// declare radius
	double area2;// declare area

	// assign radius
	radius2 = 10;
	// calculate area
	area2 = radius2 * radius2 * 3.14159;
	// Print area
	System.out.println("the area is" + area2);

  }
}
